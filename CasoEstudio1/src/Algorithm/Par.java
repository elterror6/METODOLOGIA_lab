package Algorithm;

public class Par {
	private long time;
	private int n;
	
	public Par(long time, int n) {
		this.time=time;
		this.n=n;
	
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}
}
